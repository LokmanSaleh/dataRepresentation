	public static String GET_RELATIONAL_DB_STUCTURE_SQL = 	
			"	\r\n" + 
			"	SELECT \r\n" + 
			"		TABLE_NAME as tableName,\r\n" + 
			"		COLUMN_NAME as columnName, \r\n" + 
			"		(CASE \r\n" + 
			"				WHEN ( EXISTS (\r\n" + 
			"\r\n" + 
			"					select * \r\n" + 
			"					From INFORMATION_SCHEMA.KEY_COLUMN_USAGE as B\r\n" + 
			"					WHERE \r\n" + 
			"					REFERENCED_TABLE_SCHEMA = ? and \r\n" + 
			"					columnName = B.COLUMN_NAME and \r\n" + 
			"					tableName = B.TABLE_NAME\r\n" + 
			"\r\n" + 
			"				)\r\n" + 
			"					 ) THEN true\r\n" + 
			"				ELSE false\r\n" + 
			"		END) as isforeignkey,\r\n" + 
			"		\r\n" + 
			"      	 (select REFERENCED_TABLE_NAME \r\n" + 
			"						From INFORMATION_SCHEMA.KEY_COLUMN_USAGE as B\r\n" + 
			"						WHERE \r\n" + 
			"						REFERENCED_TABLE_SCHEMA = ? and \r\n" + 
			"						columnName = B.COLUMN_NAME and \r\n" + 
			"						tableName = B.TABLE_NAME\r\n" + 
			"			) as parentTable, \r\n" + 
			"            \r\n" + 
			"          (select REFERENCED_COLUMN_NAME\r\n" + 
			"						From INFORMATION_SCHEMA.KEY_COLUMN_USAGE as B\r\n" + 
			"						WHERE \r\n" + 
			"						REFERENCED_TABLE_SCHEMA = ? and \r\n" + 
			"						columnName = B.COLUMN_NAME and \r\n" + 
			"						tableName = B.TABLE_NAME\r\n" + 
			"			) as parentColumn\r\n" + 
			" \r\n" + 
			" \r\n" + 
			"	FROM\r\n" + 
			"    \r\n" + 
			"		INFORMATION_SCHEMA.columns\r\n" + 
			"\r\n" + 
			"	WHERE \r\n" + 
			"\r\n" + 
			"		TABLE_SCHEMA = ? ";


	public static String INSERT_CHAINE_SQL = " INSERT INTO chaines (chaine) \r\n " + 
										 " VALUES (?)";
	
	public static String GET_CHAINES_SQL = " SELECT * FROM chaines";
	
			
// Functions call data base 
	/**
	 * 
	 * @return
	 * @throws SQLException
	 */
	public ResultSet getRelationalDbStucture() throws SQLException {

		PreparedStatement stmt = con.prepareStatement(GET_RELATIONAL_DB_STUCTURE_SQL);

		stmt.setString(1, dataBaseName);
		stmt.setString(2, dataBaseName);
		stmt.setString(3, dataBaseName);
		stmt.setString(4, dataBaseName);

		return stmt.executeQuery();
	}

	
	/**
	 * Get the list of chaines 
	 * @return
	 * @throws SQLException 
	 */
	public ResultSet getChaines() throws SQLException { 
		
		PreparedStatement stmt = con.prepareStatement(GET_CHAINES_SQL);
 
		return stmt.executeQuery();
	}

	
	
	/**
	 * Insert the chaine
	 * @param chaine
	 */
	public void insertChaine(String chaine) {

		PreparedStatement preparedStatement;
		try {

			preparedStatement = con.prepareStatement(INSERT_CHAINE_SQL);
			preparedStatement.setString(1, chaine);
			preparedStatement.executeUpdate();

		} catch (SQLException e) {

			e.printStackTrace();
		}

	}
	
